#===============================================================================
# CLASS
#===============================================================================
class GuessWord:
   """A Guess that represents guessing a word for the current Hangman game"""

   #-----------------------------------------------------------------------------
   # ctor
   #-----------------------------------------------------------------------------
   def __init__(self, guess):
      """save word guess to class variable on init"""
      self.guess = guess


   #-----------------------------------------------------------------------------
   # make the guess
   #-----------------------------------------------------------------------------
   def makeGuess(self, game):
      """Tell the game to guess this object's word"""
      game.guessWord(self.guess)


   #-----------------------------------------------------------------------------
   # print out function
   #-----------------------------------------------------------------------------
   def __str__(self):
      """GuessWord representation as a string"""
      return "GuessWord[" + self.guess + "]"




