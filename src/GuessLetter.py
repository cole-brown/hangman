#===============================================================================
# CLASS
#===============================================================================
class GuessLetter:
   """A Guess that represents guessing a letter for the current Hangman game"""

   #-----------------------------------------------------------------------------
   # ctor
   #-----------------------------------------------------------------------------
   def __init__(self, guess):
      """save letter guess to class variable on init"""
      self.guess = guess


   #-----------------------------------------------------------------------------
   # make the guess
   #-----------------------------------------------------------------------------
   def makeGuess(self, game):
      """Tell the game to guess this object's letter"""
      game.guessLetter(self.guess)


   #-----------------------------------------------------------------------------
   # print out function
   #-----------------------------------------------------------------------------
   def __str__(self):
      """GuessLetter representation as a string"""
      return "GuessLetter[" + self.guess + "]"




